// map mode toggle
const modeMapCheckbox = document.getElementById("mode-map");
const mapContainer = document.getElementById("map-container");
const modeMap = () => {
  if (modeMapCheckbox.checked != true) {
    mapContainer.setAttribute("class", "show-map");
  } else {
    mapContainer.removeAttribute("class", "show-map");
  }
}

if (modeMapCheckbox && mapContainer) {
  modeMapCheckbox.onchange = function() {modeMap()};
}

